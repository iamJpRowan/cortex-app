#!/usr/bin/env node

/**
 * Build User Docs
 *
 * Reads docs/user/*.md, parses frontmatter (title, summary), extracts heading
 * sections with slugified IDs, and emits src/renderer/src/lib/user-docs.generated.ts
 * for the renderer to consume (manifest + full/section content).
 */

const fs = require('fs')
const path = require('path')

const DOCS_DIR = path.resolve(__dirname, '../docs/user')
const OUT_FILE = path.resolve(__dirname, '../src/renderer/src/lib/user-docs.generated.ts')

function slugify(text) {
  return text
    .toLowerCase()
    .replace(/\s+/g, '-')
    .replace(/[^a-z0-9-]/g, '')
    .replace(/-+/g, '-')
    .replace(/^-|-$/g, '')
}

function parseFrontmatter(raw) {
  const match = raw.match(/^---\r?\n([\s\S]*?)\r?\n---\r?\n([\s\S]*)$/)
  if (!match) return { title: '', summary: '', body: raw }
  const fm = match[1]
  const body = match[2].trim()
  let title = ''
  let summary = ''
  for (const line of fm.split('\n')) {
    const t = line.match(/^title:\s*["']?([^"'\n]+)["']?/)
    const s = line.match(/^summary:\s*["']?([^"'\n]+)["']?/)
    if (t) title = t[1].trim()
    if (s) summary = s[1].trim()
  }
  return { title, summary, body }
}

const HEADING_RE = /^(#{1,6})\s+(.+)$/gm

function extractSections(body) {
  const sections = []
  let lastEnd = 0
  let lastLevel = 0
  let lastId = ''
  let lastTitle = ''
  let match
  HEADING_RE.lastIndex = 0
  while ((match = HEADING_RE.exec(body)) !== null) {
    const level = match[1].length
    const title = match[2].trim()
    const id = slugify(title)
    const start = match.index
    if (lastId) {
      const content = body.slice(lastEnd, start).trim()
      sections.push({ id: lastId, title: lastTitle, content })
    }
    lastEnd = match.index + match[0].length
    lastLevel = level
    lastId = id
    lastTitle = title
  }
  if (lastId) {
    const content = body.slice(lastEnd).trim()
    sections.push({ id: lastId, title: lastTitle, content })
  }
  return sections
}

function extractToc(body) {
  const toc = []
  let match
  HEADING_RE.lastIndex = 0
  while ((match = HEADING_RE.exec(body)) !== null) {
    const level = match[1].length
    const title = match[2].trim()
    const id = slugify(title)
    toc.push({ id, title, level })
  }
  return toc
}

function escapeForTs(str) {
  return str
    .replace(/\r\n/g, '\n')
    .replace(/\\/g, '\\\\')
    .replace(/`/g, '\\`')
    .replace(/\$/g, '\\$')
}

function build() {
  if (!fs.existsSync(DOCS_DIR)) {
    fs.mkdirSync(DOCS_DIR, { recursive: true })
  }
  const files = fs
    .readdirSync(DOCS_DIR)
    .filter(f => f.endsWith('.md'))
    .sort()
  const manifest = {}
  const docEntries = []

  for (const file of files) {
    const slug = file.replace(/\.md$/, '')
    const raw = fs.readFileSync(path.join(DOCS_DIR, file), 'utf8')
    const { title, summary, body } = parseFrontmatter(raw)
    const toc = extractToc(body)
    const sections = extractSections(body)
    const sectionIds = toc.map(t => t.id)

    manifest[slug] = {
      title: title || slug,
      summary,
      sectionIds,
      sections: toc.map(t => ({ id: t.id, title: t.title, level: t.level })),
    }
    docEntries.push({
      slug,
      title: title || slug,
      summary,
      bodyEscaped: escapeForTs(body),
      sections: sections.map(s => ({
        id: s.id,
        title: s.title,
        contentEscaped: escapeForTs(s.content),
      })),
    })
  }

  const outLines = [
    '// Generated by scripts/build-user-docs.js — do not edit by hand.',
    '',
    'export interface UserDocSection { id: string; title: string; level: number }',
    'export interface UserDocManifestEntry { title: string; summary: string; sectionIds: string[]; sections: UserDocSection[] }',
    'export type UserDocManifest = Record<string, UserDocManifestEntry>',
    '',
    'export const userDocsManifest: UserDocManifest = ' +
      JSON.stringify(manifest, null, 2) +
      ' as UserDocManifest',
    '',
    'const docBodies: Record<string, string> = {}',
    'const docSections: Record<string, Array<{ id: string; title: string; content: string }>> = {}',
    '',
  ]

  for (const d of docEntries) {
    outLines.push(`docBodies['${d.slug}'] = \`${d.bodyEscaped}\``)
    outLines.push(`docSections['${d.slug}'] = [`)
    for (const s of d.sections) {
      outLines.push(
        `  { id: ${JSON.stringify(s.id)}, title: ${JSON.stringify(s.title)}, content: \`${s.contentEscaped}\` },`
      )
    }
    outLines.push(']')
    outLines.push('')
  }

  outLines.push(
    '/** Full markdown body for a doc. */',
    'export function getDocBody(slug: string): string | undefined { return docBodies[slug] }',
    '',
    '/** Short summary for tooltips (from frontmatter). */',
    'export function getDocSummary(slug: string): string | undefined { return userDocsManifest[slug]?.summary }',
    '',
    '/** Section content (markdown) for a doc; for modal or deep link. */',
    'export function getDocSectionContent(slug: string, sectionId: string): string | undefined {',
    '  const list = docSections[slug]; if (!list) return undefined',
    '  const s = list.find(x => x.id === sectionId); return s?.content',
    '}',
    '',
    '/** Ordered list of slugs for TOC. */',
    'export const userDocSlugs: string[] = ' +
      JSON.stringify(docEntries.map(e => e.slug)),
    ''
  )

  fs.writeFileSync(OUT_FILE, outLines.join('\n'), 'utf8')
  console.log('Wrote', OUT_FILE, '(', docEntries.length, 'docs)')
}

build()
